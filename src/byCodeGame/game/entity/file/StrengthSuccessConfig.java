public class StrengthSuccessConfig {
	private int lv;
	private int rate;
	private int iron;
	private int addRate;
	public int getLv() {
		return lv;
	}
	public void setLv(int lv) {
		this.lv = lv;
	}
	public int getRate() {
		return rate;
	}
	public void setRate(int rate) {
		this.rate = rate;
	}
	public int getIron() {
		return iron;
	}
	public void setIron(int iron) {
		this.iron = iron;
	}
	public int getAddRate() {
		return addRate;
	}
	public void setAddRate(int addRate) {
		this.addRate = addRate;
	}
	
	
}
