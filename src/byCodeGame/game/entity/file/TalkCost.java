package byCodeGame.game.entity.file;

public class TalkCost {
	/** 会谈的次数			*/
	private int talksNumber;
	/** 会谈的花费			*/
	private int cost;
	/** 可以连续会谈的次数	*/
	private int multipleNumber;
	
	
	public int getTalksNumber() {
		return talksNumber;
	}
	public void setTalksNumber(int talksNumber) {
		this.talksNumber = talksNumber;
	}
	public int getCost() {
		return cost;
	}
	public void setCost(int cost) {
		this.cost = cost;
	}
	public int getMultipleNumber() {
		return multipleNumber;
	}
	public void setMultipleNumber(int multipleNumber) {
		this.multipleNumber = multipleNumber;
	}
	
	
	
	
}
