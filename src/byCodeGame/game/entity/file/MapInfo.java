package byCodeGame.game.entity.file;

public class MapInfo {
	/** 地图id			*/
	private int id;
	/** 国籍				*/
	private int nation;
	/** 地图名字			*/
	private String mapName;
	/** 声望ID			*/
	private int reputationID;
	/** 英雄普通			*/
	private String heroSetSoso;
	/** 道具普通			*/
	private String itemSetSoso;
	/** 装备普通			*/
	private String equipSetSoso;
	/** 英雄优秀			*/
	private String heroSetGood;
	/** 道具优秀			*/
	private String itemSetGood;
	/** 装备优秀			*/
	private String equipSetGood;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getNation() {
		return nation;
	}
	public void setNation(int nation) {
		this.nation = nation;
	}
	public String getMapName() {
		return mapName;
	}
	public void setMapName(String mapName) {
		this.mapName = mapName;
	}
	public int getReputationID() {
		return reputationID;
	}
	public void setReputationID(int reputationID) {
		this.reputationID = reputationID;
	}
	public String getHeroSetSoso() {
		return heroSetSoso;
	}
	public void setHeroSetSoso(String heroSetSoso) {
		this.heroSetSoso = heroSetSoso;
	}
	public String getItemSetSoso() {
		return itemSetSoso;
	}
	public void setItemSetSoso(String itemSetSoso) {
		this.itemSetSoso = itemSetSoso;
	}
	public String getEquipSetSoso() {
		return equipSetSoso;
	}
	public void setEquipSetSoso(String equipSetSoso) {
		this.equipSetSoso = equipSetSoso;
	}
	public String getHeroSetGood() {
		return heroSetGood;
	}
	public void setHeroSetGood(String heroSetGood) {
		this.heroSetGood = heroSetGood;
	}
	public String getItemSetGood() {
		return itemSetGood;
	}
	public void setItemSetGood(String itemSetGood) {
		this.itemSetGood = itemSetGood;
	}
	public String getEquipSetGood() {
		return equipSetGood;
	}
	public void setEquipSetGood(String equipSetGood) {
		this.equipSetGood = equipSetGood;
	}
	
}
