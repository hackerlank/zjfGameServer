package byCodeGame.game.entity.file;

public class TavernConfig {
	private int rowID;
	private int setID;
	private int id;
	private int type;
	private int num;
	private int probWeight;
	private int lvRange;
	private int country;
	private String remark;

	public int getRowID() {
		return rowID;
	}

	public void setRowID(int rowID) {
		this.rowID = rowID;
	}

	public int getSetID() {
		return setID;
	}

	public void setSetID(int setID) {
		this.setID = setID;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public int getProbWeight() {
		return probWeight;
	}

	public void setProbWeight(int probWeight) {
		this.probWeight = probWeight;
	}

	public int getLvRange() {
		return lvRange;
	}

	public void setLvRange(int lvRange) {
		this.lvRange = lvRange;
	}

	public int getCountry() {
		return country;
	}

	public void setCountry(int country) {
		this.country = country;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

}
