package byCodeGame.game.entity.file;
/**
 * 带属性道具
 * @author wcy 2016年5月13日
 *
 */
public class Prop2Config {
	/**道具id*/
	private int id;
	/**道具类型*/
	private int type;
	/**道具名称*/
	private String name;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
